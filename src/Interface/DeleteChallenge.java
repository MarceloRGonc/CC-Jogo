    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        this.desafio = des.getText();
        
        if(!this.desafio.equals("")){                
            PDU receive;
            List<Fields> campos = null;
            try {
                ArrayList<Fields> list = new ArrayList<>();
                list.add(new Fields(7, this.desafio));
                byte[] r = Communication.serializa(list);
                PDU a = new PDU(10,list.size(),r.length,list);
                this.s.sendPDU(a); 
                receive = this.s.receivePDU();
                campos = receive.getCampos();
            } catch (IOException ex) { new OkDialog(this).show("Ocorreu um erro!!"); }
            
            if(campos != null) {
                if(campos.get(campos.indexOf(new Fields(0,""))).getInfo().equals("OK")) {
                    String n = campos.get(campos.indexOf(new Fields(7,""))).getInfo();
                    String d = campos.get(campos.indexOf(new Fields(4,""))).getInfo();
                    String h = campos.get(campos.indexOf(new Fields(5,""))).getInfo();

                    new OkDialog(this).show("O desafio " + n + " foi apagado!");
                    MainMenu mm = new MainMenu(this.s, this.username);
                    mm.setVisible(true);
                    this.dispose();
                } else { 
                    new OkDialog(this).show("NÃ£o conseguiu apagar o desafio!");
                    MainMenu mm = new MainMenu(this.s, this.username);
                    mm.setVisible(true);
                    this.dispose();
                } 
            }   
        }              
    }//GEN-LAST:event_deleteActionPerformed

